package com.lucasdnd.serversimulator.ui;

/**
 * Button click interface. Use it to define click action using inner classes, for example:
 * 
 * button.setClickListener(new ButtonClickListener() {
 * 		@Override
 * 		public void onClick() {
 * 			// Button action here
 * 		}
 * }
 * 
 * @author lucasdnd
 *
 */
public interface ButtonClickListener {
	public void onClick();
}
